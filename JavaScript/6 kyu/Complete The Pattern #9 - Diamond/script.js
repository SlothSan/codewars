const pattern = (n) => {
  let res = [];
  let spaces = 2 * n - 1;
  for (let i = 1; i <= n; i++) {
    let term = Array.from({ length: i }, (_, index) => index + 1).concat(
      Array.from({ length: i - 1 }, (_, index) => i - index - 1)
    );
    term = term.map((t) => t % 10);
    term = Array((spaces - term.length) >> 1)
      .fill(" ")
      .concat(term, Array((spaces - term.length) >> 1).fill(" "));
    term = term.join("");
    res.push(term);
  }
  let res2 = [...res].reverse().slice(1);
  res = res.concat(res2);
  return res.join("\n");
};

